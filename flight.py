class Flight:
    def __init__(self, heading, alt, speed_x, speed_y, lat, long):
        self.heading = heading
        self.alt = alt
        self.spee_x = speed_x
        self.speed_y = speed_y
        self.lat = lat
        self.long = long

    def get_heading(f):
        return f.heading
    
    def get_alt(f):
        return f.alt
    
    def get_speed_x(f):
        return f.speed_x
    
    def get_speed_y(f):
        return f.speed_y
    
    def get_lat(f):
        return f.lat
    
    def get_long(f):
        return f.long